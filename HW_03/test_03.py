from random import randint


def bubbleSort(arr1, st, arr2=None):
    n = len(arr1)

    if arr2 is None:
        if st == '<':
            for i in range(n):
                for j in range(0, n - i - 1):
                    if arr1[j] < arr1[j + 1]:
                        arr1[j], arr1[j + 1] = arr1[j + 1], arr1[j]
        if st == '>':
            for i in range(n):
                for j in range(0, n - i - 1):
                    if arr1[j] > arr1[j + 1]:
                        arr1[j], arr1[j + 1] = arr1[j + 1], arr1[j]
    if arr2 is not None:
        for i in range(n):
            for j in range(0, n - i - 1):
                if arr2[j] > arr2[j + 1]:
                    arr1[j], arr1[j + 1] = arr1[j + 1], arr1[j]
                    arr2[j], arr2[j + 1] = arr2[j + 1], arr2[j]


def show(arr, a=2, rv=False):
    if not rv:
        if a == 1:
            for item in arr:
                print("{:>5}".format(item), end='')
        if a == 2:
            for i in range(len(arr)):
                for j in range(len(arr[i])):
                    #   print(f'{arr[i][j]:>5}', end='')
                    print("{:>5}".format(arr[i][j]), end='')
                print()
        print()
    elif rv:
        n = len(arr)

        for i in range(n):
            for j in range(i, n):
                if i == j:
                    continue
                else:
                    arr[i][j], arr[j][i] = arr[j][i], arr[i][j]


